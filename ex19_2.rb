def banana_and_apple(banana_count, apple_count)
  puts "I do have #{banana_count} bananas!"
  puts "I also do have #{apple_count} apple fruits."
  puts "These are enough fruits to host my friends."
  puts "So that we can stay healthy throughout our journey."
  puts # a blank line
end

banana_and_apple(20, 25)

number_of_banana = 25
number_of_apple = 30
banana_and_apple(number_of_banana, number_of_apple)

banana_and_apple(30-5, 40-10)

banana_and_apple(number_of_banana + 25, number_of_apple + 30)

banana_and_apple(5*4, 6*4)
